public class SwitchCase {
    public static void main(String[] args) {
        int n=4;
        switch (n) {
            case 1:
                System.out.println("blue");
            case 2:
                System.out.println("pink");
            case 3:
                System.out.println("red");
            case 4:
                System.out.println("orange");
        }
    }
}
